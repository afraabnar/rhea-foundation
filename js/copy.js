function renderSiteCopy() {
  const elements = document.querySelectorAll('[data-copy]');
  elements.forEach(el => {
    const path = el.getAttribute('data-copy').split('.');
    let value = window.siteCopy;
    for (const key of path) {
      if (value == null) break;
      value = value[key];
    }
    if (value == null) return;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = value;
    } else if (el.tagName === 'OPTION') {
      el.textContent = value;
      if (el.value === '') el.value = '';
    } else {
      el.textContent = value;
    }
  });

  const htmlElements = document.querySelectorAll('[data-copy-html]');
  htmlElements.forEach(el => {
    const path = el.getAttribute('data-copy-html').split('.');
    let value = window.siteCopy;
    for (const key of path) {
      if (value == null) break;
      value = value[key];
    }
    if (value == null) return;
    el.innerHTML = value;
  });

  const optionGroups = document.querySelectorAll('[data-copy-options]');
  optionGroups.forEach(select => {
    const path = select.getAttribute('data-copy-options').split('.');
    let options = window.siteCopy;
    for (const key of path) {
      if (options == null) break;
      options = options[key];
    }
    if (!Array.isArray(options)) return;
    select.innerHTML = options.map(option => `<option value="${option.value}">${option.label}</option>`).join('');
  });
}

window.addEventListener('DOMContentLoaded', renderSiteCopy);
